
#include <iostream>
using namespace std;

int main()
{
	int w;

	cout<<"Enter the Week Number (1-7)";
	cin>>w;

	if (w==1){
			cout<<"Monday";}
	else if (w==2){
			cout<<"Tuesday";}
	else if (w==3){
			cout<<"Wednesday";}
	else if (w==4){
			cout<<"Thursday";}
	else if (w==5){
			cout<<"Friday";}
	else if (w==6){
			cout<<"Saturday";}
	else if (w==7){
			cout<<"Sunday";}
	else {
			cout<<"Invalid Input";}

	}
